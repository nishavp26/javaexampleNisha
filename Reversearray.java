package examples;

public class Reversearray {
	
	public static void main(String[] args) {
		


	int a[]= {36,12,45,67,11};
	
	for(int i = 0; i<a.length;i++) {
		
		
	System.out.println(a[i]);
	
		
	}
	System.out.println();
	
	
	for(int j=a.length-1;j>=0;j--) {
		
		System.out.println(a[j]);
	}

	

}
}
